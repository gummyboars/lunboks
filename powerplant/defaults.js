function drawDefaultCities(cityDefaults, cityConnections, radius, renderWidth, renderHeight) {
  for (let [city1, city2, cost] of cityConnections) {
    let loc1 = cityDefaults[city1];
    let loc2 = cityDefaults[city2];
    ctx.save();
    ctx.lineWidth = radius / 2;
    ctx.strokeStyle = "darkgray";
    ctx.beginPath();
    ctx.moveTo(loc1.x * renderWidth, loc1.y * renderHeight);
    ctx.lineTo(loc2.x * renderWidth, loc2.y * renderHeight);
    ctx.closePath();
    ctx.stroke();

    if (cost != 0) {
      let smallRadius = cost <= 8 ? (radius/2) : (2 * radius / 3);
      ctx.beginPath();
      ctx.arc((loc1.x + loc2.x) * renderWidth / 2, (loc1.y + loc2.y) * renderHeight / 2, smallRadius, 0, 2 * Math.PI, true);
      ctx.closePath();
      ctx.fillStyle = "darkgray";
      ctx.fill();
    }
    ctx.restore();
  }

  for (let city in cityDefaults) {
    ctx.save();
    ctx.translate(cityDefaults[city].x * renderWidth, cityDefaults[city].y * renderHeight);
    ctx.beginPath();
    ctx.arc(0, 0, radius, 0, 2 * Math.PI, true);
    ctx.closePath();
    ctx.fillStyle = "gray";
    ctx.fill();

    ctx.fillStyle = "white";
    ctx.fillRect(-5 * radius / 4, 3 * radius / 5, 5 * radius / 2, 2 * radius / 5);
    let newFontSize = getTextSize(ctx, city, 5*radius/2, 2 * radius / 5);
    ctx.font = newFontSize + "px sans-serif";
    ctx.textAlign = "center";
    ctx.textBaseline = "middle";
    ctx.fillStyle = "black";
    ctx.fillText(city, 0, 4 * radius / 5);
    ctx.restore();
  }

  for (let [city1, city2, cost] of cityConnections) {
    if (cost == 0) {
      continue;
    }
    let loc1 = cityDefaults[city1];
    let loc2 = cityDefaults[city2];
    ctx.save();
    let newFontSize = getTextSize(ctx, cost, 2 * radius / 3, 2 * radius / 3);
    ctx.font = newFontSize + "px sans-serif";
    ctx.textAlign = "center";
    ctx.textBaseline = "middle";
    ctx.fillStyle = "black";
    ctx.fillText(cost, (loc1.x + loc2.x) * renderWidth / 2, (loc1.y + loc2.y) * renderHeight / 2);
    ctx.restore();
  }
}

